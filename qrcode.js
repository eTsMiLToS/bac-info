function makeAnimation() {
    console.log("animation started");
    var turtle = new Turtle();
    turtle.speed(10);
    turtle.penup();
    turtle.goto(-100,0);
    turtle.pendown();
    turtle.color("red");
    turtle.write(" <3 ADMIS BAC 2023 <3 ", align="center", font=("Arial", 18, "normal"));
  }
  